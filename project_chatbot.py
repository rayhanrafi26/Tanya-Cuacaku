# 5. Helper — Ekstraksi output bersih
# ------------------------------------------
def ekstrak_teks_jawaban(output_agent) -> str:
    """
    Menangani perbedaan format output antar versi model Gemini.
    Gemini 3.x mengembalikan output sebagai list of content blocks (karena reasoning/thinking
    internal model), sedangkan versi lama mengembalikan string langsung.
    Fungsi ini menyeragamkan keduanya menjadi string biasa siap-tampil.
    """
    if isinstance(output_agent, str):
        return output_agent
    if isinstance(output_agent, list):
        return "\n".join(
            block.get("text", "")
            for block in output_agent
            if isinstance(block, dict) and block.get("type") == "text"
        )
    return str(output_agent)

