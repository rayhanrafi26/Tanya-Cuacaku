import requests
import json

def cek_cuaca_bmkg(kode_wilayah: str) -> str:
    """
    Mengambil data prakiraan cuaca dari Open Data BMKG.
    Membutuhkan kode_wilayah tingkat IV (contoh Kemayoran: '31.71.03.1001').
    """
    url = f"https://api.bmkg.go.id/publik/prakiraan-cuaca?adm4={kode_wilayah}"

    try:
        # Mengirim permintaan ke server BMKG
        response = requests.get(url)
        response.raise_for_status() # Akan memicu error jika gagal (misal kena rate limit/salah kode)
        data = response.json()

        # Ekstraksi informasi lokasi
        lokasi = data['data'][0]['lokasi']

        # Ekstraksi prakiraan cuaca (Kita ambil 4 data pertama saja = 12 jam ke depan)
        # Tujuannya agar Gemini tidak kebingungan membaca data 3 hari sekaligus
        prakiraan = data['data'][0]['cuaca'][0][:4]

        # Gabungkan data yang relevan
        data_relevan = {
            "informasi_lokasi": lokasi,
            "prakiraan_cuaca": prakiraan
        }

        # Kembalikan dalam bentuk string JSON agar mudah dikunyah oleh Gemini
        return json.dumps(data_relevan, indent=2)

    except Exception as e:
        return f"Terjadi kesalahan saat mengambil data dari BMKG: {str(e)}"

