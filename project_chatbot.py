def setup_rag_pdf(pdf_path: str):
    print(f"Sedang membaca dokumen: {pdf_path}...")

    loader = PyPDFLoader(pdf_path)
    dokumen = loader.load()

    text_splitter = RecursiveCharacterTextSplitter(chunk_size=1000, chunk_overlap=200)
    chunks = text_splitter.split_documents(dokumen)
    print(f"Dokumen berhasil dipotong menjadi {len(chunks)} bagian teks kecil.")

    # KODE YANG MERUBAH SEGALANYA: Menggunakan model lokal HuggingFace
    print("Mengunduh model vektor lokal dan memproses dokumen (hanya butuh beberapa detik)...")
    embeddings = HuggingFaceEmbeddings(model_name="sentence-transformers/all-MiniLM-L6-v2")

    vector_db = FAISS.from_documents(chunks, embeddings)
    print("✅ Database Vektor berhasil dibuat dan siap digunakan!")

    return vector_db

